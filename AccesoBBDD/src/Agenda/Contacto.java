package Agenda;

/**
 *
 * @author toti
 */
public class Contacto {
    private int id;
    private String nombre, apellido;
    private int edad;

    public Contacto(int id, String nombre, String apellido, int edad) {
        this.id = id;
        this.nombre = nombre;
        this.apellido = apellido;
        this.edad = edad;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public int getId() {
        return id;
    }

    @Override
    public String toString() {
        return String.format("%-2d %-15s %-20s %2d", id,nombre,apellido,edad);
    }
    
    
    
}
